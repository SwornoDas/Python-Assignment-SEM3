# Program to prompt user to enter first name and last name and display a greeting message

# Input first name from the user
fName = input("Enter Your First Name : ")
# Input last name from the user
lName = input("Enter Your Last Name : ")

# Display greeting with first name and last name separated by a space
print("Greetings!!!",fName,lName)
# Display greeting with first name and last name concatenated (no space)
print("Greetings!!!",fName+lName)

