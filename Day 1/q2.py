# Program to perform addition, subtraction, multiplication, division
# integer division and modulo division on two integer numbers

# Input two integer numbers from the user
a = int(input("Enter first number. "))
b = int(input("Enter second number. "))

# Perform and display addition
print("Addition :", a+b)
# Perform and display subtraction
print("Subtraction :", a-b)
# Perform and display multiplication
print("Multiplication :", a*b)
# Perform and display division (floating point)
print("Division :", a/b)
# Perform and display integer division (quotient)
print("Integer Division :", a//b)
# Perform and display modulo division (remainder)
print("Modulo :", a%b)